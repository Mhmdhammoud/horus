/**
 * Regenerate src/migrations-bundle.ts from packages/db/drizzle/*.sql.
 *
 * Run after `pnpm --filter @horus/db generate` adds a new drizzle migration, so the
 * embedded-pglite local database (createLocalDb) stays in sync with the postgres-js
 * schema. The published CLI bundle has no drizzle/ folder on disk, so the SQL must be
 * inlined here.
 *
 * Usage: pnpm --filter @horus/db gen:migrations
 */
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const drizzleDir = resolve(here, '../drizzle');
const out = resolve(here, '../src/migrations-bundle.ts');

// Order migrations by their numeric tag prefix (0000_, 0001_, ...).
const tags = readdirSync(drizzleDir)
  .filter((f) => f.endsWith('.sql'))
  .map((f) => f.replace(/\.sql$/, ''))
  .sort();

const migrations = tags.map((tag) => {
  const sql = readFileSync(resolve(drizzleDir, `${tag}.sql`), 'utf-8');
  const statements = sql
    .split('--> statement-breakpoint')
    .map((s) => s.trim())
    // Drop empties and comment-only chunks (e.g. the 0006 no-op migration).
    .filter((s) => s.length > 0 && !/^(--[^\n]*\n?)*$/.test(s));
  return { tag, statements };
});

const header = `/**
 * AUTO-GENERATED — do not edit by hand.
 * Generated by \`pnpm --filter @horus/db gen:migrations\` from packages/db/drizzle/*.sql.
 *
 * The published CLI bundle (apps/horus/dist/index.cjs) ships as a single file with
 * NO node_modules and NO drizzle/ folder, so the embedded-pglite local database
 * cannot read migrations from disk. Instead the migration SQL is inlined here and
 * applied idempotently by createLocalDb() (see client.ts).
 */

export interface EmbeddedMigration {
  /** Drizzle migration tag, e.g. "0000_redundant_kabuki". */
  readonly tag: string;
  /** Individual SQL statements (drizzle statement-breakpoints already split out). */
  readonly statements: readonly string[];
}

export const EMBEDDED_MIGRATIONS: readonly EmbeddedMigration[] = ${JSON.stringify(
  migrations,
  null,
  2,
)};
`;

writeFileSync(out, header);
const total = migrations.reduce((n, m) => n + m.statements.length, 0);
console.log(`wrote ${out} — ${migrations.length} migrations, ${total} statements`);
